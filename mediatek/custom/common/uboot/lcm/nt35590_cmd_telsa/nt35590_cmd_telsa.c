static void lcm_get_params(LCM_PARAMS *params)
{
	memset(params, 0, sizeof(LCM_PARAMS));
	params->type 						= LCM_TYPE_DSI;
	params->width 						= FRAME_WIDTH;
	params->height	    				= FRAME_HEIGHT;

	// enable tearing-free
	params->dbi.te_mode 				= LCM_DBI_TE_MODE_VSYNC_ONLY;
	params->dbi.te_edge_polarity		= LCM_POLARITY_RISING;

//#if (LCM_DSI_CMD_MODE)
	params->dsi.mode   					= CMD_MODE;
//#else
//	params->dsi.mode   = SYNC_PULSE_VDO_MODE;
//#endif
	// DSI
	/* Command mode setting */
	params->dsi.LANE_NUM 		 		= LCM_FOUR_LANE;
	//The following defined the fomat for data coming from LCD engine.
	params->dsi.data_format.color_order = LCM_COLOR_ORDER_RGB;
	params->dsi.data_format.trans_seq   = LCM_DSI_TRANS_SEQ_MSB_FIRST;
	params->dsi.data_format.padding     = LCM_DSI_PADDING_ON_LSB;
	params->dsi.data_format.format      = LCM_DSI_FORMAT_RGB888;

	params->dsi.word_count				= 720*3;	//DSI CMD mode need set these two bellow params, different to 6577
	params->dsi.vertical_active_line	= 1280;

	// Video mode setting 
	params->dsi.intermediat_buffer_num 	= 0;
	params->dsi.PS						= LCM_PACKED_PS_24BIT_RGB888;

	params->dsi.PLL_CLOCK 				= LCM_DSI_6589_PLL_CLOCK_240_5;//this value must be in MTK suggested table
	
	//bit rate
	params->dsi.pll_div1=0;		// div1=0,1,2,3;div1_real=1,2,4,4
	params->dsi.pll_div2=1;		// div2=0,1,2,3;div1_real=1,2,4,4
	params->dsi.fbk_div =20;	// fref=26MHz, fvco=fref*(fbk_div+1)*2/(div1_real*div2_real)		
}

static void lcm_init(void)
{
	SET_RESET_PIN(1);
	MDELAY(10);
	SET_RESET_PIN(0);
	MDELAY(2);
	SET_RESET_PIN(1);
	MDELAY(10);

	push_table(lcm_initialization_ret, sizeof(lcm_initialization_ret)/sizeof(struct LCM_setting_table), 1);
}

static void lcm_suspend(void)
{
	unsigned int data_array[16];
	
	data_array[0]=0x00280500; 
	dsi_set_cmdq(data_array, 1, 1);
	MDELAY(10);

	data_array[0] = 0x00100500; 
	dsi_set_cmdq(data_array, 1, 1);
	MDELAY(120);//delay more for 3 frames time  17*3=54ms

	SET_RESET_PIN(0);//Reset Low
	MDELAY(10);
	
	//push_table(lcm_deep_sleep_mode_in_setting, sizeof(lcm_deep_sleep_mode_in_setting)/sizeof(struct LCM_setting_table), 1);
}

static void lcm_resume(void)
{
	unsigned int data_array[16];
	
	lcm_init();

	data_array[0]=0x00110500;
	dsi_set_cmdq(&data_array,1,1);
	MDELAY(120);
	data_array[0]=0x00290500;
	dsi_set_cmdq(&data_array,1,1);
	MDELAY(20);
}

